import arxiv

@tool
def busca_arxiv(query: str) -> str:
    """Busca artículos científicos en arXiv sobre un tema específico y devuelve sus títulos y resúmenes."""
    client = arxiv.Client()
    search = arxiv.Search(
        query=query,
        max_results=3,
        sort_by=arxiv.SortCriterion.Relevance
    )
    resultados = []
    for paper in client.results(search):
        resultados.append(f"Título: {paper.title}\nResumen: {paper.summary[:300]}...\nURL: {paper.entry_id}")
    return "\n\n".join(resultados) if resultados else "No se encontraron resultados."

