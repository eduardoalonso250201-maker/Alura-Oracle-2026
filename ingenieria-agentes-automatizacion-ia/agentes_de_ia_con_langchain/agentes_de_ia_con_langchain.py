"""Nodo supervisor para combinar respuestar"""

def supervisor_node(state: AgentState) -> dict:
  #se inicia por dar info de como comportarse
  #este se puede ver como agente que revisa y combina la respuesta final
  """
  Combina los resultados de los agentes web y cientificos y formatea la respuesta final.

  Args:
    state (AgentState): El estado actual del agente, que contiene web_answer y scientific_answer.
  Returns:
    dict: Un diccionario que contiene la respuesta final combinada y formateada.
  """

  #Ahora se hace definicion por si algo no sale bien el codigo
  web_results = str(state.get("web_answer", "No se realizó búsqueda web."))
  #              0                 1                     2
  #0 convierte la salida en str, 1 se corre si no hubo error, el 2 se activa si hubo error
  scientific_results = str(state.get("scientific_answer", "No se realizó búsqueda científica."))

  #formatacion de respuesta final
  final_answer = '## Resultados combinados de búsqueda\n\n' #primero se imprime este str
  final_answer += '### Resultados desde la Web\n\n' + web_results + '\n\n' #se anade esto
  final_answer += '### Artículos científicos (arXiv)\n\n' + scientific_results #luego esto

  return {"final_answer": final_answer}

"""Se crea ahora si un flujo para los dos agentes y luego se llamra a esta funcion que los combina a los dos"""

#inciando por la creacion del flujo
workflow = StateGraph(AgentState)

#aqui se ve como se define un flow con varios agentes
#primero se definen los nombres con sus respectivas funciones
workflow.add_node("web_search", funcion_agente_web)
workflow.add_node("scientific_search", funcion_agente_cientifico)
workflow.add_node("supervisor", supervisor_node)

#aqui se define el flujo como tal, donde inicia y donde termina
workflow.add_edge(START, "web_search")
workflow.add_edge("web_search", "scientific_search")
workflow.add_edge("scientific_search", "supervisor")
workflow.add_edge("supervisor", END)

#se hace la compilacion
app = workflow.compile()

#Crear la nueva demostracion visual con este nuevo flujo
Image(app.get_graph().draw_mermaid_png())

#Ahora si se activa el flujo, dando el request y se imprime la respuesta
resultado = app.invoke({"user_query": "Cuál el impacto de la IA en la medicina?"})
print(resultado["final_answer"])

"""Implementacion de router para eleccion de agente segun la pregunta"""

def router_agent(state: AgentState) -> dict:
  #al igual que los otros agentes se da un promt para que sepa su funcionamiento
  router_prompt = """
  Eres un agente ruteador. Tu tarea es decidir cuál agente es más apropiado
  para responder la pregunta del usuario.

  Pregunta del usuario: {user_query}

  Puedes elegir entre las siguientes opciones:
  - web_search: Usa esta opción si la pregunta requiere una búsqueda general en internet.
  - scientific_search: Usa esta opción si la pregunta requiere artículos científicos.

  Responde ÚNICAMENTE con el nombre de la opción (web_search o scientific_search).
  """

  #se crea el agente con sus caracteristicas
  router_agent_instance = create_react_agent(
      model=llm,
      tools=[], #Notar que las tools estan vacias porque este agente no necesita herramienta externa, razona con el puro texto
      prompt=router_prompt
  )

  #Se invoca la llm con el agente creado
  response = router_agent_instance.invoke({
      "messages": [("user", state["user_query"])]
  })

  #se crea variable que contiene solo la parte del mensaje que nos interesa, con formactacion de texto
  decision = response["messages"][-1].content.strip().lower()

  #se retorna el resultado basado en contenido
  #como este agente responde con web_search o scientific_search
  #la respuesta de este agente regresa el diccionario con key "router_decision" y value la decision
  if decision == "web_search":
      return {"router_decision": "web_search"}
  elif decision == "scientific_search":
      return {"router_decision": "scientific_search"}

  # Por defecto, usamos web_search
  return {"router_decision": "web_search"}

#crear un nuevo workflow
workflow = StateGraph(AgentState)

#definir los nodos
workflow.add_node("router", router_agent)
workflow.add_node("web_search", funcion_agente_web)
workflow.add_node("scientific_search", funcion_agente_cientifico)
workflow.add_node("supervisor", supervisor_node)

#se define el punto de inicio del flujo y el router condicional
workflow.add_edge(START, "router") #indica inicio de flujo
workflow.add_conditional_edges( #notar que en lugar de edge es conditional para agregar un nodo condicional
    "router",
    lambda state: state["router_decision"],
    {
        "web_search": "web_search",
        "scientific_search": "scientific_search",
    }
)
#aqui cambia el flujo tmn, ya no se indica que cuando termina un agente inicie el otro, mas bien se indica
#que si el valor es "web_search" o "scientific_search" directo a supervisor
#y una vez activado el supervisor se termina
workflow.add_edge("web_search", "supervisor")
workflow.add_edge("scientific_search", "supervisor")
workflow.add_edge("supervisor", END)

#compilacion de flujo
app = workflow.compile()

#visualizacion de grapho
Image(app.get_graph().draw_mermaid_png())

