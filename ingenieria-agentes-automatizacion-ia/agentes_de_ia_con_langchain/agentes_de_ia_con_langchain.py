"""Ahora haremos una estrutura de comunicacion entre resultado busqueda web y articulo cientifico"""

from typing import TypedDict

#clase para combinar respuestas de agentes
#recibe como argumento el modulo typedDict
class AgentState(TypedDict):
    #se definen los formatos de entrada y salida
    user_query: str
    web_answer: str
    scientific_answer: str
    final_answer: str

#funciones que encapsulan la respuesta del agente web y otra para cientifico

#primero se hara la que encapsula al agente web
#en la primera linea se ve que se generara una lista con datos estructurados tipo JSON
def funcion_agente_web(state: AgentState) -> dict:
    #definir comportamiento del agente
    """
    Función que ejecuta un agente al estilo ReAct que realiza una búsqueda en la web
    sobre un término específico.

    Esta funcion toma el estado actual del agente (que contiene la pregunta del usuario)
    y crea un agente utilizando Gemini y la herramienta Tailvy, y retorna la respuesta del agente.

    Arg:
        state (AgentState): Un diccionario que contiene la pregunta del usuario y la
        respuesta del agente

    Returns:
        dict: la respuesta del agente.
    """

    #a partir de aqui se pone lo mismo que cuando se comenzo a configurar el agente busca_web
    #el siguiente promt puede ser el mismo que se uso en la parte donde se llama el aegnte_web
    system_prompt = """
    Actúa como un asistente útil.
    Usa las herramientas proporcionadas para responder a las preguntas del usuario.

    - busca_web: Retorna resultados de búsqueda web.

    Cuando el usuario pregunte sobre un tema específico, usa busca_web y devuelve enlaces en la respuesta.
    """
    #se le dan criterios al agente los mismos que habiamos puesto
    agente_web = create_react_agent(
        model=llm,
        tools=tools,
        prompt=system_prompt
    )

    #notar que en esta parte que se corre al agente, ahora el input o el request no es un texto explicito
    #ahora se espera que el usuario lo introduzca
    resultado = agente_web.invoke({
        "messages": [("user", state["user_query"])]
    })

    #se le da formato a la respuesta final, igual que antes slicing de values de "messages"
    respuesta_final = resultado["messages"][-1].content
    if isinstance(respuesta_final, list):
      respuesta_final = respuesta_final[0]["text"]
    #aqui se dice que la salida sera un diccionario con su key web_ansswer y el value la respuesta final
    return {"web_answer": respuesta_final}

